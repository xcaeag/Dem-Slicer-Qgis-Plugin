        self.points['Y'].setXY(self.x('X'), self.y('X') - height)

        # peak
        self.points['peak'].setXY(self.milieu('A', 'C'))

        self.points['R'].setXY(self.x('L') + width/2, self.y('C'))

        # perspective handles
        a = self.angle('D', 'Y', 'H')
        self.points['B2'].setXY(self.rotatePoint('H', a, 'Y'))
